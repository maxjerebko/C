#include <iostream>  // Підключення бібліотеки для введення/виведення
using namespace std;  // Щоб не писати std:: перед cout тощо

int main() {
    int table[10][10] = { };  // Створення двовимірного масиву 10x10, заповненого нулями

    int* ptr = &table[0][0];  // Вказівник на перший елемент масиву

    for (int row = 0; row < 10; row++) {  // Проходимо кожен рядок
        for (int col = 0; col < 10; col++) {  // Проходимо кожен стовпець
            *(ptr + row * 10 + col) = (row + 1) * (col + 1);  // Записуємо добуток номера рядка і стовпця
        }
    }

    for (int row = 0; row < 10; row++) {  // Виведення кожного рядка
        for (int col = 0; col < 10; col++) {  // Виведення кожного елемента рядка
            cout.width(4);  // Встановлення ширини виводу для форматування
            cout << *(ptr + row * 10 + col);  // Вивід елемента таблиці
        }
        cout << endl;  // Перехід на новий рядок
    }

    return 0;  // Завершення програми
}
