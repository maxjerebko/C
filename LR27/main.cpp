#include <iostream>
using namespace std;

int main(void) {
    int a = 8, b = 0, c = 0;

    // Зчитуємо значення для змінної b
    cin >> b;

    try {
        // Перевірка на ділення на нуль
        if (b == 0) {
            // Викидаємо виняток, якщо b == 0
            throw "Ваше введення некоректне: не можна ділити на нуль.";
        }

        // Обчислюємо результат ділення
        c = a / b;
    }
    catch (const char* msg) {
        // Обробка винятку — виведення повідомлення
        cout << msg << endl;
    }

    // Виведення результату (якщо обчислення відбулось)
    cout << c << endl;

    return 0;
}
